package Day_4;

public class Starpattern {
    public static void main(String[] args) {
        for (int i = 1; i<= 3; i++)
        {
            for (int j = 1; j<=3; j++)
            {
                System.out.print(" * "); //print the star
            }
            System.out.println(" "); //space
        }

    }
}