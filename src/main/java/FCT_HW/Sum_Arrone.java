package FCT_HW;

public class Sum_Arrone
{
    public static void main(String[] args) {
        int[] a = {10, 20, 30};
        int sum = 0;

        for (int i = 0; i < a.length; i++)
        {

            sum += a[i];

        System.out.println("The sum of all elements in the array is: " + sum);
    }
}
}
